package javabasicsobsqura;

class DoWhileLoopSample
{
  public static void main(String args[])
  {
  
    int a=1;
   do
     {
	   System.out.println(a);
	   a++;
     }while(a<=10);
	 
       System.out.println("Exited do-while loop");
	   
	   int b=10;
     do
     {
	   System.out.println(b);
	   b--;
     }while(b>=1);
	 
       System.out.println("Exited do-while loop");
	
		
	}

}